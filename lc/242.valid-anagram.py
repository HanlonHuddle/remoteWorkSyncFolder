class Solution:
    def isAnagram(self, s, t):
        """
        :type s: str
        :type t: str
        :rtype: bool
        """
        if len(s) != len(t):
            return False
            
        cs = collections.Counter(s)
        ct = collections.Counter(t)

        for k in cs:
            if cs[k] != ct[k]:
                return False
        
        return True
